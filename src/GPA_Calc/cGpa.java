    private void entActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entActionPerformed
        // TODO add your handling code here:
        
        
        if( level.getSelectedItem().equals("Choose one..."))
        {
        
            JOptionPane.showMessageDialog(null, " Level is not Selected");
        }
        
        else if(sem.getSelectedItem().equals("Choose one..."))
        {
        
            JOptionPane.showMessageDialog(null, " Semester is not Selected");
        }
        
        else if (gpa.getText().isEmpty()){
        
        
            JOptionPane.showMessageDialog(null, " Enter your GPA for the Semester");
        }
        
        else {
            
            double myGpa = Double.parseDouble(gpa.getText());
            
            if(myGpa >5.0){
            
                JOptionPane.showMessageDialog(null, "GPA is Greater than 5.0");
                 level.setSelectedItem("Choose one...");
        sem.setSelectedItem("Choose one...");
        gpa.setText(null);
                
            }
            
            else{
                  model.insertRow(model.getRowCount(), new Object[]
        { 
        
         level.getSelectedItem(),
             sem.getSelectedItem(),
         gpa.getText()
        
        });
   
        level.setSelectedItem("Choose one...");
        sem.setSelectedItem("Choose one...");
        gpa.setText(null);
            }
        
        
        }
        
       
    }//GEN-LAST:event_entActionPerformed

