    public float sumAll (){
                float rowsCount = jTable1.getRowCount();
                float sum =0;
                for (int i =0; i<rowsCount; i++){
                
                    sum = sum+Float.parseFloat((String) jTable1.getValueAt(i, 2));
                }
             
            
            
            return sum;
                    }
    
